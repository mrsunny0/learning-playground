// defines
#define F_CPU 16000000UL

// includes
#include <avr/io.h>
#include <util/delay.h>

/************************************************************************/
/* Setup                                                                */
/************************************************************************/
void setup() {
	DDRD = (1<<PORTD7);
}

/************************************************************************/
/* Main                                                                 */
/************************************************************************/
int main(void)
{
	setup();
    while (1) 
    {
		PORTD ^= (1<<PORTD7);
		_delay_ms(100);
    }
}

